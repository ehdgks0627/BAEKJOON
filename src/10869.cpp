#include <iostream>
using namespace std;

int main(int argc, char *argv[])
{
    int num[2];
    cin >> num[0] >> num[1];
    cout << num[0] + num[1] << endl;
    cout << num[0] - num[1] << endl;
    cout << num[0] * num[1] << endl;
    cout << num[0] / num[1] << endl;
    cout << num[0] % num[1];
    return 0;
}
